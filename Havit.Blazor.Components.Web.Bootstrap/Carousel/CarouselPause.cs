namespace Havit.Blazor.Components.Web.Bootstrap;

/// <summary>
/// Behavior of the <see cref="HxCarousel"/>.
/// </summary>
public enum CarouselPause
{
	/// <summary>
	/// The carousel will stop sliding on hover.
	/// </summary>
	Hover = 0,

	/// <summary>
	/// The carousel won't stop sliding on hover.
	/// </summary>
	False = 1
}
